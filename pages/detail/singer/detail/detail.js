const app = getApp();
const audio = wx.getBackgroundAudioManager();       //获取全局唯一的背景音频管理器
var commonJs = require('../../../common/common.js');

Page({

  /**
   * 页面的初始数据
   */
  data: {
    // 播放控件数据
    data: {
      playSong: false,  //是否显示控件
      playSongInfo: {},  //正在播放音频信息
      playSongClass: ["iconfont icon-icon-play", "iconfont icon-zanting"] //当前播放状态对应的类名数组
    },
    page: 0,
    songsList: [],
    introDetail: ["introDetail", ''],
    iconClass: ["iconfont icon-icon-xia", "iconfont icon-icon"]
  },

  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    wx.setNavigationBarTitle({
      title: options.title
    })
    this.setData({
      singerId: options.singerid
    })
    commonJs.onBackgroundAudioStop(this, app, audio)
    this.singerInfoRequest(options.singerid)
  },

  //请求歌手个人信息的方法
  singerInfoRequest(singerId) {
    wx.showLoading({
      title: 'Loading'
    })
    const that = this;
    let page = this.data.page + 1;
    wx.request({
      url: "http://m.kugou.com/singer/info/" + singerId + "&json=true&page=" + page,
      method: "GET",
      header: {
        "contentType": "json"
      },
      success(res) {
        let singerInfo = res.data.info;
        singerInfo.imgurl = singerInfo.imgurl.split('{size}').join('400');
        let songsList = that.data.songsList.concat(res.data.songs.list);
        that.setData({
          page: page,
          singerInfo: singerInfo,
          songsList: songsList,
          total: res.data.songs.total
        })
        // let singerInfo = mydata.info;
        // singerInfo.imgurl = singerInfo.imgurl.split('{size}').join('400');
        // that.setData({
        //     singerInfo: singerInfo,
        //     songsList: mydata.songs.list 
        // })
      },
      complete(){
        wx.hideLoading();
      }
    })
  },

  //显示个人简介所有信息
  seeAllIntro() {
    this.data.iconClass.unshift(this.data.iconClass.pop());
    this.data.introDetail.unshift(this.data.introDetail.pop());
    this.setData({
      introDetail: this.data.introDetail,
      iconClass: this.data.iconClass
    })
  },

  /**
   * 页面上拉触底事件的处理函数
   */
  onReachBottom: function () {
    if(this.data.total <= this.data.songsList.length)return;
    this.singerInfoRequest(this.data.singerId);
  },

  /**
   * 生命周期函数--监听页面显示
   */
  onShow: function () {
    commonJs.onShow(this);
  },

  //播放歌曲的方法
  playSong(e) {
    if (e.currentTarget) {
      commonJs.setGlobalData(this.data.songsList, app);  //将当前歌曲所在列表存进全局变量中
    }
    commonJs.playSong(e, this);
  },

  //暂停播放音乐
  pauseAudio() {
    commonJs.pauseAudio(this, audio);
  },

  // 播放下一首
  nextAudio() {
    commonJs.nextAudio(this, app)
  },

  // 收藏歌曲到个人中心
  addMyList(e) {
    commonJs.addMyList(e)
  },

  //跳转到当前播放列表
  goListening() {
    commonJs.goListening(this, app)
  }

})